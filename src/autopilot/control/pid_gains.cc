#include "pid_gains.h"

Debug& operator<<(Debug& dbg, const pid_gains& gains)
{
	for (boost::array<double, 3>::const_iterator it=gains.gains.begin(); it != gains.gains.end()-1; ++it)
		dbg << *it << ", ";
	return dbg << gains.gains.back();
}
